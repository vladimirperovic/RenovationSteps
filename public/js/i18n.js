const VALID_LANGS = ['en', 'sr', 'ru', 'zh'];
const VALID_CURS  = ['rsd', 'eur'];
let currentLang = localStorage.getItem('planner_lang') || localStorage.getItem('landing_lang');
if (!VALID_LANGS.includes(currentLang)) currentLang = 'en';
let currentCur = localStorage.getItem('planner_cur');
if (!VALID_CURS.includes(currentCur)) currentCur = 'eur';

function t(key) { 
  return (LANG[currentLang] && LANG[currentLang][key]) || (LANG.en && LANG.en[key]) || key; 
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('planner_lang', lang);
  localStorage.setItem('landing_lang', lang);
  applyI18n();
  render();
}

function setCur(cur) {
  currentCur = cur;
  localStorage.setItem('planner_cur', cur);
  applyI18n();
  render();
}

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-opt]').forEach(el => {
    el.textContent = t(el.dataset.i18nOpt);
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPh);
  });
  
  document.querySelectorAll('.bl-lang-btn[data-lang]').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === currentLang);
  });
  document.querySelectorAll('.bl-lang-btn[data-cur]').forEach(b => {
    b.classList.toggle('active', b.dataset.cur === currentCur);
  });

  const activeTab = document.querySelector('.bl-nav-item.active');
  if (activeTab) {
    const key = 'nav_' + activeTab.dataset.tab;
    const topBarTitle = document.getElementById('blTopbarTitle');
    if (topBarTitle) topBarTitle.textContent = t(key);
  }
  
  // Update Selects in Header
  const selL = document.getElementById('blLangSelect');
  if (selL) selL.value = currentLang;
  const selC = document.getElementById('blCurSelect');
  if (selC) selC.value = currentCur;
  
  

  // Sync any remaining buttons (Settings Tab)
  document.querySelectorAll('.bl-lang-btn[data-lang]').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === currentLang);
  });
  document.querySelectorAll('.bl-lang-btn[data-cur]').forEach(b => {
    b.classList.toggle('active', b.dataset.cur === currentCur);
  });

  const langMap = { en: 'en', sr: 'sr', ru: 'ru', zh: 'zh-CN' };
  document.documentElement.lang = langMap[currentLang] || 'en';
}

// Initial call
applyI18n();

document.querySelectorAll('.bl-lang-btn[data-lang]').forEach(btn => {
  btn.addEventListener('click', () => setLang(btn.dataset.lang));
});

document.querySelectorAll('.bl-lang-btn[data-cur]').forEach(btn => {
  btn.addEventListener('click', () => setCur(btn.dataset.cur));
});

